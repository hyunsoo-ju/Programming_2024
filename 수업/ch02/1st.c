#include <stdio.h> // preprocessor directive -> include the contents of the file stdio.h
// anything that starts with # is a preprocessor directive
// stdio.h is a header file that contains the declarations of the standard input/output library functions
// stdio stands for standard input/output

int main() // main is a funtion with return type of interger
{
    printf("hello world\n"); 
    /*
    printf is a function that prints the string "hello world" to the standard output
    -> takes a string as an argument / it is defined in stdio.h
    */
    return 0;                // return 0 to the operating system
    
}

